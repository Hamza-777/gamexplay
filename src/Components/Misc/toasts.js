import { toast } from 'react-toastify';
import 'react-toastify/dist/ReactToastify.css';

const infoPopup = (message) => {
  toast.info(message, {
    position: 'top-center',
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
  });
};

